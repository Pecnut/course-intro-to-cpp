#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    float x;
    cout << "Enter a number" << endl;
    cin >> x;
    cout << "That number squared is " << pow(x,2) << endl;
    cout << "The square root of that number is " << sqrt(x) << endl;
    cout << "The sine of that number is " << sin(x) << endl;
    return 0;
}
